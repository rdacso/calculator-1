def add(num1, num2):
	"""adds two numbers together"""
	return num1 + num2 

def subtract(num1, num2):
	"""subracts one number from the other number"""
	return num1 - num2

def multiply(num1, num2):
	"""multiples two numbers together"""
	return num1 * num2

def divide(num1, num2):
	"""divides one number by another number"""
	return float(num1) / float(num2)

def square(num1):
	"""Squaring a number"""
	return num1**2
    
def cube(num1):
	"""Cubing a number"""
	return num1**3

def power(num1, num2):
	"""Raising a number to the power of another number"""
	return num1 ** num2

def mod(num1, num2):
	"""Returning the remainder of one number divided by another"""
	return num1 % num2
